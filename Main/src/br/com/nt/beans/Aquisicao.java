package br.com.nt.beans;

public class Aquisicao {
	private int id;
	private String leite;
	private String cafe;
	private String cerveja;
	private String pao;
	private String manteiga;
	private String arroz;
	private String feijao;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLeite() {
		return leite;
	}
	public void setLeite(String leite) {
		this.leite = leite;
	}
	public String getCafe() {
		return cafe;
	}
	public void setCafe(String cafe) {
		this.cafe = cafe;
	}
	public String getCerveja() {
		return cerveja;
	}
	public void setCerveja(String cerveja) {
		this.cerveja = cerveja;
	}
	public String getPao() {
		return pao;
	}
	public void setPao(String pao) {
		this.pao = pao;
	}
	public String getManteiga() {
		return manteiga;
	}
	public void setManteiga(String manteiga) {
		this.manteiga = manteiga;
	}
	public String getArroz() {
		return arroz;
	}
	public void setArroz(String arroz) {
		this.arroz = arroz;
	}
	public String getFeijao() {
		return feijao;
	}
	public void setFeijao(String feijao) {
		this.feijao = feijao;
	}
	
}
